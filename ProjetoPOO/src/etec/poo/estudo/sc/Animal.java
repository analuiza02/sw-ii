package etec.poo.estudo.sc;

public abstract class Animal {
	
	private String comer;
	private String viver;
	
	public String getComer() {
		return comer;
	}
	public void setComer(String comer) {
		this.comer = comer;
	}
	public String getViver() {
		return viver;
	}
	public void setViver(String viver) {
		this.viver = viver;
	}
	
	

}
